import 'package:flutter/material.dart';

/// Body region definition with position and size for clickable areas
class BodyRegion {
  final String id;
  final double xPercent;
  final double yPercent;
  final double widthPercent;
  final double heightPercent;

  const BodyRegion({
    required this.id,
    required this.xPercent,
    required this.yPercent,
    required this.widthPercent,
    required this.heightPercent,
  });

  /// Convert percentage coordinates to actual pixel coordinates
  Rect toRect(Size imageSize) {
    return Rect.fromLTWH(
      imageSize.width * xPercent / 100,
      imageSize.height * yPercent / 100,
      imageSize.width * widthPercent / 100,
      imageSize.height * heightPercent / 100,
    );
  }

  /// Check if a point is within this body region
  bool contains(Offset point, Size imageSize) {
    final rect = toRect(imageSize);
    return rect.contains(point);
  }
}

