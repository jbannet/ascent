  /// Get body regions for specified gender
  static Map<String, List<BodyRegion>> getRegionsForGender(String? gender) {
    switch (gender?.toLowerCase()) {
      case 'female':
        return womanBodyRegions;
      case 'male':
      default:
        return manBodyRegions; // Default to male
    }
  }

  /// Get all body parts available for mapping
  static List<String> getAllBodyParts() {
    // Combine all body parts from both gender maps
    final allParts = <String>{};
    allParts.addAll(manBodyRegions.keys);
    allParts.addAll(womanBodyRegions.keys);
    return allParts.toList()..sort();
  }

  /// Find which body part contains a given point
  static String? findBodyPartAt(Offset point, Size imageSize, String? gender) {
    final regions = getRegionsForGender(gender);
    
    for (final entry in regions.entries) {
      final bodyPart = entry.key;
      final bodyRegions = entry.value;
      
      for (final region in bodyRegions) {
        if (region.contains(point, imageSize)) {
          return bodyPart;
        }
      }
    }
    
    return null;
  }
